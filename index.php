In PHP, a namespace is a mechanism that allows you to organize code into logical groups and avoid naming conflicts between different parts of your code. Namespaces provide a way to encapsulate your code and prevent naming collisions with other code libraries, classes, functions, and constants.

Namespaces are declared at the beginning of a file using the "namespace" keyword:

Note: A namespace declaration must be the first thing in the PHP file.

Any code that follows a namespace declaration is operating inside the namespace, so classes that belong to the namespace can be instantiated without any qualifiers. To access classes from outside a namespace, the class needs to have the namespace attached to it.

