// Arithmetic operators: : +, -, *, /, %,**
$x = 3;
$y= 2;
//-> Addition(+)
echo $x + $y;   // 5
echo("\n");

//-> Subtraction(-)
echo $x - $y;   // 1
echo("\n");

//-> Multiplication(*)
echo $x * $y;   // 6
echo("\n");

//-> Division(/)
echo $x / $y;   // 1.5
echo("\n");

//-> Modulus(%)
echo $x % $y;   // 1
echo("\n");

//-> Exponentiation(**)
echo $x ** $y;  // 9
echo("\n");

